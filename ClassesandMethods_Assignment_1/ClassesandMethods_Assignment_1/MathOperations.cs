using System;
using System.Collections.Generic;
using System.Text;

namespace ClassesandMethods_Assignment_1
{
    class MathOperations
    {
        public int Add(int data, int value)
        {
            return data + value;

        }

        public int Subtract(int data, int value)
        {
            return data - value;
        }

        public int Divide(int data, int value)
        {
            return data / value;

        }

        public double Decimal(double data, double value)
        {
            return data * value;
        }
    }
}
